"""
You have to find maximum sum of numbers from top left corner
of a matrix to bottom right corner.
You can only go down or right.
"""


def find_sum(matrix):
    new = [0] * (len(matrix) + 1)
    for line in matrix:
        for i, elem in enumerate(line, 1):
            new[i] = elem + max(new[i - 1], new[i])
    return new[-1]


